
TITLE DISPLAY PROGRAMMING IS FUN ONE BY ONE
.MODEL SMALL
.STACK 64
.DATA 
STR DB "PROGRAMMING IS FUN"
LEN DW $-STR
.CODE
	MAIN PROC FAR
	MOV AX, @DATA
	MOV DS, AX
	MOV CX, LEN
L1:   MOV DL, STR[SI]
	MOV AH, 02H
        INC SI
	INT 21H
        LOOP L1
	MOV AH, 4CH
	INT 21H
	MAIN ENDP
END
