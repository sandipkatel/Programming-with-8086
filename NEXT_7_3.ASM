TITLE ADD 10 16 BIT NUMS IN TABLE FORMAT

.MODEL SMALL
.STACK 64
.DATA
	TABLE1 DW 1234H, 0343H, 0234H, 3244H, 0325H, 24E1H, 3432H, 2444H, 3233H, 5322H
	TABLE2 DW 1234H, 0325H, 2431H, 3432H, 24D4H, 3233H, 0AAAAH, 213EH, 54ABH, 0ABCDH
	TABLE3 DW 10 DUP(?)
.CODE
MAIN PROC FAR
	MOV AX, @DATA
	MOV DS, AX
	MOV CX, 000AH
	MOV SI, 00H
NEXT: MOV AX, TABLE1[SI]
	ADD AX, TABLE2[SI]
	MOV TABLE3[SI], AX
	ADD SI, 02H
	LOOP NEXT
	MOV AX, 4C00H
	INT 21H
MAIN ENDP
END