TITLE PROGRAM TO DISPLAY STRING IN VIDEO MODE
.MODEL SMALL
.STACK 64
.DATA
	MAXLEN DB 14
	ACTLEN DB ?
	STR DB 14 DUP('$')
.CODE
MAIN PROC FAR
	MOV AX, @DATA
	MOV DS, AX
	
	;Take input
	MOV DX, OFFSET MAXLEN
	MOV AH, 0AH
	INT 21H

	;Clear the screen
	MOV AX, 0003H
	INT 10H

	;OPEN SCREEN OF 20 X 20
	MOV AL, 00H
	MOV CH, 03
	MOV CL, 10
	MOV DH, 23
	MOV DL, 30
	MOV BH, 61H
	MOV AH, 06H
	INT 10H


	;Calculate center of screen (START + END - LENGTH)/2
	MOV BH, 00
	MOV AH, 00
	MOV DH, 13
	MOV AL, DL
	ADD AL, CL
	SUB AL, ACTLEN
	MOV DL, 02
	DIV DL
	MOV DL, AL
	MOV AX, 0200H
	INT 10H

	MOV DX, OFFSET STR
	MOV AH, 09H
	INT 21H
	
	MOV AH, 4CH
	INT 21H
MAIN ENDP
END MAIN


