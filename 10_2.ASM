TITLE TAKE INPUT n AND DISPLAY SUM OF MULTIPLICATION UPTO n
.MODEL SMALL
.STACK 64
.DATA
	MAXLEN DB 4
	ACTLEN DB ?
	STRNUM DB 4 DUP('$')
	RESNUM DW ?
	RES DB 8 DUP('$')
.CODE
MAIN PROC FAR
		MOV AX, @DATA
		MOV DS, AX
		
		MOV DX, OFFSET MAXLEN
		MOV AH, 0AH
		INT 21H
		
		CALL STR2NUM
		
		MOV CX, AX
		MOV BX, 0000H
		MOV DX, 02H
CONTINUE: MOV AL, DL
		INC DL
		MUL DL
		ADD BX, AX
		INC DL
		LOOP CONTINUE 
		
		MOV RESNUM, BX
		CALL NUM2STR

		MOV AX, 0200H
		MOV DX, 0DH
		INT 21H
		MOV DX, 0AH
		INT 21H

		MOV DX, OFFSET 	RES
		MOV AH, 09H
		INT 21H

		MOV AH, 4CH
		INT 21H
MAIN ENDP

STR2NUM PROC NEAR
	MOV CX, 0000H
	MOV CL, ACTLEN
	MOV AX, 0000H
	MOV DX, AX      
  	MOV BX, 10
	LEA SI, STRNUM
REPEAT:MUL BX
	MOV DL, [SI]
	SUB DX, 30H
	ADD AX, DX
	INC SI
  	LOOP REPEAT
  RET 
STR2NUM ENDP

NUM2STR PROC NEAR
	MOV BX, 10
	MOV AX, RESNUM
	MOV DI, 0080H

NEXT: XOR DX, DX
		DIV BX
		ADD DX, 30H
		MOV [DI], DX
		INC DI
		CMP AX, 00
		JNE NEXT

		;REVERSE THE STRING
		MOV CX, DI
		MOV SI, DI
		SUB CX, 80H
		MOV DI, OFFSET RES
ASCII:	DEC SI
		MOV AL, [SI]
		MOV [DI], AL
		INC DI
		LOOP ASCII
		RET
	
END

