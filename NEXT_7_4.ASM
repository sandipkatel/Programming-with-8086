TITLE CHECK 10 16 BIT NUMS
.MODEL SMALL
.STACK 64
.DATA
	TABLE1 DW 1234H, 0343H, 0234H, 3244H, 0325H, 24E1H, 3432H, 2444H, 3233H, 5322H
	TABLE2 DW 1234H, 0325H, 2431H, 3432H, 24D4H, 3233H, 0AAAAH, 213EH, 54ABH, 0ABCDH
	TABLE3 DW 10 DUP(?)
.CODE
MAIN PROC FAR
	MOV AX, @DATA
	MOV DS, AX
	MOV CX, 000AH
	MOV SI, 00H
NEXT: MOV AX, TABLE1[SI]
	CMP AX, TABLE2[SI]
	JNC NONZERO
	MOV TABLE3[SI], 1FFFH
	JMP OK
NONZERO: MOV TABLE3[SI], 0000H
OK:	ADD SI, 02H
	LOOP NEXT

	MOV AX, 4C00H
	INT 21H
MAIN ENDP
END