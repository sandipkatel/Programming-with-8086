TITLE PROGRAM TO DISPLAY STRING IN VIDEO MODE
.MODEL SMALL
.STACK 64
.DATA
	MAXLEN DB 60
	ACTLEN DB ?
	STR DB 60 DUP('$')
.CODE
MAIN PROC FAR
	MOV AX, @DATA
	MOV DS, AX
	
	;Take input
	MOV DX, OFFSET MAXLEN
	MOV AH, 0AH
	INT 21H

	;Clear the screen
	MOV AX, 0003H
	INT 10H
	
	MOV SI, OFFSET STR
	MOV DX, 0000H
	MOV CL, ACTLEN
	
NEXT:	MOV DL, [SI]
	CMP DL, ' '
	JNZ PRINT
	INC DH
	MOV DL, ACTLEN
	SUB DL, CL
	MOV AX, 0200H
	INT 10H
	DEC ACTLEN ; TO AVOID ' ' IN NEXT WORD
	JMP NWORD
PRINT:MOV AH, 02H
	INT 21H
NWORD:INC SI
	LOOP NEXT
	
	MOV AH, 4CH
	INT 21H
MAIN ENDP
END MAIN


