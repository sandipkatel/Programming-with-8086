TITLE PROGRAM TO DISPLAY STRING IN VIDEO MODE
.MODEL SMALL
.STACK 64
.DATA
	MAXLEN DB 60
	ACTLEN DB ?
	STR DB 60 DUP('$')
.CODE
MAIN PROC FAR
	MOV AX, @DATA
	MOV DS, AX
	
	;Take input
	MOV DX, OFFSET MAXLEN
	MOV AH, 0AH
	INT 21H

	;Clear the screen
	MOV AX, 0003H
	INT 10H
	
	;Calculate center screen (End + beg - stringlen)/02
	MOV BH, 00
	MOV DH, 00
	MOV AL, 80
	MOV DL, ACTLEN
	SUB AL, DL
	MOV DL, 02H
	DIV DL
	MOV DL, AL
	MOV AH, 02H
	INT 10H

	LEA DX, STR
	MOV AH, 09H
	INT 21H

	MOV AH, 4CH
	INT 21H
MAIN ENDP
END MAIN
