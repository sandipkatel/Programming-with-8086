TITLE TAKE INPUT n AND DISPLAY SUM OF MULTIPLICATION UPTO n
.MODEL SMALL
.STACK 64
.DATA
	TABLE DW 2234H, 0343H, 0234H, 0244H, 1325H, 14E1H, 5432H, 14F4H, 0233H, 3022H
	SUM DW ?
	RESSUM DB 10 DUP('$')
.CODE
MAIN PROC FAR
		MOV AX, @DATA
		MOV DS, AX
		
		MOV SI, OFFSET TABLE
		MOV BX, 0
		MOV DX, 0
		MOV CX, 10
CHECK: 	ADD BX, [SI]
		ADD SI, 02
		LOOP CHECK 
		
		MOV SUM, BX
		CALL NUM2STR

		MOV DX, OFFSET 	RESSUM
		MOV AH, 09H
		INT 21H

		MOV AH, 4CH
		INT 21H
MAIN ENDP

NUM2STR PROC NEAR
	MOV BX, 10
	MOV AX, SUM
	MOV DI, 0080H

NEXT: XOR DX, DX
	DIV BX
	ADD DX, 30H
	MOV [DI], DX
	INC DI
	CMP AX, 00
	JNE NEXT

	;REVERSE THE STRING
	MOV CX, DI
	MOV SI, DI
	SUB CX, 80H
	MOV DI, OFFSET RESSUM
ASCII:	DEC SI
	MOV AL, [SI]
	MOV [DI], AL
	INC DI
	LOOP ASCII
	RET
NUM2STR ENDP
END