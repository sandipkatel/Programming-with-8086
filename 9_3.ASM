TITLE PROGRAM TO DISPLAY STRING IN VIDEO MODE
.MODEL SMALL
.STACK 64
.DATA
	MAXLEN DB 60
	ACTLEN DB ?
	STR DB 60 DUP('$')
.CODE
MAIN PROC FAR
	MOV AX, @DATA
	MOV DS, AX
	
	;Take input
	MOV DX, OFFSET MAXLEN
	MOV AH, 0AH
	INT 21H

	;Clear the screen
	MOV AX, 0003H
	INT 10H
	
	MOV BH, 00
	MOV AL, 40
	MOV CL, ACTLEN
	MOV SI, OFFSET STR
	MOV DH, 00H

NEXT:	MOV DL, 40
	MOV AH, 02H
	INT 10H
	MOV DL, [SI]
	MOV AH, 02H
	INT 21H
 	INC SI
	INC DH
	LOOP NEXT

	MOV AH, 4CH
	INT 21H
MAIN ENDP
END MAIN
