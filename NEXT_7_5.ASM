TITLE MULTIPLICATION TABLE OF NUM1
.MODEL SMALL
.STACK 64
.DATA
	NUM1 DW 03H
	TABLE DW 10 DUP(?)
.CODE
MAIN PROC FAR
	MOV AX, @DATA
	MOV DS, AX
	MOV CX, 000AH
	MOV AL, 00H
	MOV SI, OFFSET TABLE
NEXT: ADD AL, NUM1
	MOV TABLE[SI], AL
	INC SI
	LOOP NEXT
	MOV AX, 4C00H
	INT 21H
MAIN ENDP
END