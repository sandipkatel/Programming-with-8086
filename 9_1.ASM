TITLE PROGRAM TO DISPLAY STRING IN VIDEO MODE
.MODEL SMALL
.STACK 64
.DATA
	STR DB "Programming in Assembly Language is Fun $"
.CODE
MAIN PROC FAR
	MOV AX, @DATA
	MOV DS, AX

	;Clear the screen
	MOV AX, 0003H
	INT 10H

	MOV AL, 00H
	MOV CH, 05
	MOV CL, 20
	MOV DH, 20
	MOV DL, 60
	MOV BH, 61H
	MOV AH, 06H
	INT 10H

	MOV BH, 00H
	MOV DH, 12
	MOV DL, 30
	MOV AX, 0200H
	INT 10H

	LEA DX, STR
	MOV AH, 09H
	INT 21H

	MOV AH, 4CH
	INT 21H
MAIN ENDP
END MAIN
