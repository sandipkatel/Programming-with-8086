TITLE PROGRAM TO READ AND DISPLAY STRING
.MODEL SMall
.STACK 32
.DATA
STR db 50 dup('$')
.CODE
MAIN PROC FAR
MOV AX, @DATA
MOV DS, AX

MOV AH, 0AH
LEA DX, STR
MOV STR, 40
INT 21H

MOV AH, 02H
MOV DL, 0DH
INT 21H
MOV DL, 0AH
INT 21H

LEA DX, STR + 2
MOV AH, 09H
INT 21H


MOV AX, 4C00H
INT 21H

MAIN ENDP
END