;Write a program to read a string and display each word in separate lines.
PAGE 30, 100
TITLE PROGRAM TO TAKE INPUT AND DISPLAY EACH WORD
.MODEL SMALL
.STACK 32
.DATA
	MAXLEN DB 50
	ACTLEN DB ?
	ACTSTR DB 50 DUP('$')
.CODE
NEWLINE MACRO
	MOV AH, 02H
	MOV DL, 0DH
	INT 21H
	MOV DL, 0AH
	INT 21H
ENDM

MAIN PROC FAR
	MOV AX, @DATA
	MOV DS, AX

	MOV DX, OFFSET MAXLEN
	MOV AH, 0AH
	INT 21H
	
	MOV SI, OFFSET ACTSTR

	
	NEWLINE
CHECK: MOV DL, [SI]
	CMP DL, '$'
	JE EXIT
	CMP DL, ' '
	JNE NEXT
	NEWLINE
NEXT:	INT 21H
	INC SI
	LOOP CHECK

EXIT: MOV AX, 4C00H
	INT 21H

MAIN ENDP
END MAIN

